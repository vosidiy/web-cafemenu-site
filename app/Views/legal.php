<p>Cafemenu<br>Email: vosidiy@gmail.com<br>Address: Tashkent city, Uzbekistan, PO 1000000<br>Website: <a href="https://cafemenu.site">https://cafemenu.site</a><br>Privacy Policy URL: <a href="https://cafemenu.site/privacy-policy">https://cafemenu.site/privacy-policy</a></p>
<h5 class="my-4" id="1-information-we-collect">1. Information We Collect</h5>
<p>Cafemenu does not require users to create an account and does not directly collect personal profile information such as name, password, or payment details through the app.</p>
<p>The app may process the following information in order to function:</p>
<ul>
<li>Menu endpoint URL entered or configured for the restaurant menu source</li>
<li>Menu content downloaded from the configured endpoint, such as category names, item names, descriptions, prices, logo, and menu images</li>
<li>Locally stored cart and selection data on the device</li>
<li>Cached menu files and cached images stored on the device to support faster loading and offline use</li>
<li>Basic technical and network state needed for app functionality, such as whether the device is online when refreshing menu content</li>
</ul>
<h5 class="my-4" id="2-how-we-use-information">2. How We Use Information</h5>
<p>We use this information only to operate the app and provide its core functionality, including:</p>
<ul>
<li>Downloading and displaying restaurant menu data</li>
<li>Caching menu content and images for faster performance and offline availability</li>
<li>Saving the selected menu source on the device</li>
<li>Saving cart or item selection state locally on the device</li>
<li>Refreshing menu data when requested by the user or app administrator</li>
</ul>
<h5 class="my-4" id="3-information-storage">3. Information Storage</h5>
<p>Cafemenu stores some app data locally on the user&#39;s device, including:</p>
<ul>
<li>The configured menu endpoint</li>
<li>Cached menu data</li>
<li>Cached menu images</li>
<li>Local cart data and item selections</li>
</ul>
<p>This local storage is used only for app functionality and is not sold to third parties.</p>
<h5 class="my-4" id="4-information-shared-over-the-network">4. Information Shared Over the Network</h5>
<p>When the app is configured with a menu source, it connects to that source over the network to download menu data and related images.</p>
<p>This means the operator of the configured menu endpoint may receive technical request information that is normally transmitted when a device connects to a server, such as IP address, request time, and standard server log data. That server-side handling is controlled by the operator of the menu endpoint, not by Cafemenu.</p>
<h5 class="my-4" id="5-sharing-of-information">5. Sharing of Information</h5>
<p>Cafemenu does not sell personal information.</p>
<p>Cafemenu does not share user personal data with advertising networks, data brokers, or analytics providers as part of the app&#39;s current functionality.</p>
<p>Information may be disclosed only:</p>
<ul>
<li>If required by law, regulation, or legal process</li>
<li>To protect the security, rights, or operation of the app</li>
<li>When data is handled by the configured menu-content server as described above</li>
</ul>
<h5 class="my-4" id="6-data-retention">6. Data Retention</h5>
<p>Locally stored menu data, cached images, endpoint settings, and cart data remain on the device until they are replaced, cleared, or the app is removed.</p>
<p>When a menu is refreshed successfully, older unused cached menu images may be removed from device cache as part of normal app maintenance.</p>
<p>If the app is uninstalled, locally stored app data is generally removed by the operating system, subject to device behavior.</p>
<h5 class="my-4" id="7-data-security">7. Data Security</h5>
<p>We take reasonable steps to protect information used by the app. However, no method of electronic storage or transmission is completely secure, and we cannot guarantee absolute security.</p>
<p>Users and restaurant operators are responsible for using secure and trusted menu endpoint URLs where possible.</p>
<h5 class="my-4" id="8-children-s-privacy">8. Children&#39;s Privacy</h5>
<p>Cafemenu is not specifically directed to children. We do not knowingly collect personal information from children through account registration or similar features because the app does not currently provide those features.</p>
