<h3 class="my-3" id="privacy-policy-for-cafemenu">Privacy Policy for Cafemenu</h3>
<p><strong>Effective Date:</strong> May 9, 2026</p>
<p>Cafemenu (&quot;we,&quot; &quot;us,&quot; or &quot;our&quot;) provides a restaurant menu application for Android devices. This Privacy Policy explains how the Cafemenu app collects, uses, stores, and shares information when you use the app.</p>
