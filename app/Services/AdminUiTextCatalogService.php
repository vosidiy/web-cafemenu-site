    public function __construct(
        private readonly LanguageCatalogService $languageCatalog = new LanguageCatalogService(),
        private readonly AdminLanguageService $adminLanguage = new AdminLanguageService(),
    ) {
    }

    /**
     * @return array<string, array<string, string>>
     */
    public function getSupportedTranslationsIndexed(): array
    {
        $translations = [];

        foreach (array_keys($this->languageCatalog->getSupportedLanguagesIndexed()) as $languageCode) {
            $translations[$languageCode] = $this->getTranslationsForLanguage($languageCode) ?? self::ENGLISH_TRANSLATIONS;
        }

        return $translations;
    }

    /**
     * @return array<string, string>|null
     */
    public function getTranslationsForLanguage(string $languageCode): ?array
    {
        $normalizedCode = strtolower(trim($languageCode));

        if ($normalizedCode === '' || ! $this->languageCatalog->isSupported($normalizedCode)) {
            return null;
        }

        return array_replace(self::ENGLISH_TRANSLATIONS, self::OVERRIDES[$normalizedCode] ?? []);
    }

    public function translate(string $key, ?string $languageCode = null, array $replacements = []): string
    {
        $languageCode ??= $this->adminLanguage->resolveCurrentLanguageCode();
        $translations = $this->getTranslationsForLanguage($languageCode) ?? self::ENGLISH_TRANSLATIONS;
        $text = $translations[$key] ?? self::ENGLISH_TRANSLATIONS[$key] ?? $key;

        foreach ($replacements as $token => $value) {
            $text = str_replace('{' . $token . '}', (string) $value, $text);
        }

        return $text;
    }
